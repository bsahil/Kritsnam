#code for i2c(2 sensors)

import machine, utime
#utime.sleep_ms(100)
i2c=machine.I2C(scl=machine.Pin(33),sda=machine.Pin(32),freq=200000)
a=i2c.scan()
#utime.sleep_ms(100)
i2c.writeto(a[0],'r')
utime.sleep_ms(1000)
addr1=i2c.readfrom(a[0],5)
utime.sleep_ms(1000)
i2c.writeto(a[1],'r')
utime.sleep_ms(1000)
addr2=i2c.readfrom(a[1],5)
#utime.sleep_ms(100)
print(a, addr1, addr2)
